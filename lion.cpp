/*******************************************************************************
* マクロ定義
*******************************************************************************/
#define LION_TEX_NAME	"data/TEXTURE/サバンナタイトル用/ライオン/lion.png"
#define ANIM_PAT_X (2)
#define ANIM_PAT_Y (1)
#define ANIM_CANGE_FRAME (20)
/*******************************************************************************
* クラス定義
*******************************************************************************/
//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define NUM_VERTEX		(4)			// 頂点数
#define NUM_POLYGON		(2)			// ポリゴン数
// 頂点フォーマット( 頂点座標[2D] / 頂点カラー / テクスチャ座標 )
#define	FVF_VERTEX_2D	(D3DFVF_XYZRHW | D3DFVF_DIFFUSE | D3DFVF_TEX1)
//*****************************************************************************
// 構造体定義
//*****************************************************************************

//*****************************************************************************
// 静的
//*****************************************************************************

//=============================================================================
// コンストラクタ
//=============================================================================
CLion :: CLion()
{
	m_time = 0;
	m_animPat = 0;
	
}
//=============================================================================
// デストラクタ
//=============================================================================
CLion :: ~CLion()
{
}
CLion* CLion::Create(D3DXVECTOR3 pos,D3DXVECTOR3 size)
{
	CLion *pLion;
	pLion = new CLion;
	pLion->Init(pos,size);
	
	pLion->Load(LION_TEX_NAME);
	return pLion;
}
//=============================================================================
// ポリゴンの初期化処理
//=============================================================================
HRESULT CLion :: Init(void)
{
	CScene2D::Init();


	return S_OK;
}
//=============================================================================
// ポリゴンの初期化処理(オーバーロード)
//=============================================================================
HRESULT CLion :: Init(D3DXVECTOR3 pos,D3DXVECTOR3 size)
{
	CScene2D::Init(pos,size);

	CScene2D::SetTexture(ANIM_PAT_X,ANIM_PAT_Y,m_animPat);

	return S_OK;
}
//=============================================================================
// ポリゴンの終了処理
//=============================================================================
void CLion :: Uninit(void)
{
	CScene2D::Uninit();
}
//=============================================================================
// ポリゴンの更新処理
//=============================================================================
void CLion :: Update(void)
{
	D3DXVECTOR3 posLion = GetPosition();
	D3DXVECTOR3 sizeLion = GetSize();
	m_time++;
	if(m_time%ANIM_CANGE_FRAME == 0)
	{
		m_animPat++;
	}
	if(posLion.x > 940)
	{
		posLion.x-=2;
	}
	//if(posLion.x < -sizeLion.x)
	//{
	//	posLion.x = SCREEN_WIDTH + 300/2;
	//}
	CScene2D::SetTexture(ANIM_PAT_X,ANIM_PAT_Y,m_animPat);

	this->CLion::SetPosition(posLion);
}
//=============================================================================
// ポリゴンの描画処理
//=============================================================================
void CLion :: Draw(void)
{
	CScene2D::Draw();
}