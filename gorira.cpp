/*******************************************************************************
* マクロ定義
*******************************************************************************/
#define GORIRA_TEX_NAME	"data/TEXTURE/サバンナタイトル用/ゴリラ/gori.png"
#define ANIM_PAT_X (4)
#define ANIM_PAT_Y (1)
#define ANIM_CANGE_FRAME (20)
/*******************************************************************************
* クラス定義
*******************************************************************************/
//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define NUM_VERTEX		(4)			// 頂点数
#define NUM_POLYGON		(2)			// ポリゴン数
// 頂点フォーマット( 頂点座標[2D] / 頂点カラー / テクスチャ座標 )
#define	FVF_VERTEX_2D	(D3DFVF_XYZRHW | D3DFVF_DIFFUSE | D3DFVF_TEX1)
//*****************************************************************************
// 構造体定義
//*****************************************************************************

//*****************************************************************************
// 静的
//*****************************************************************************

//=============================================================================
// コンストラクタ
//=============================================================================
CGorira :: CGorira()
{
	m_time = 0;
	m_animPat = 0;
	
}
//=============================================================================
// デストラクタ
//=============================================================================
CGorira :: ~CGorira()
{
}
CGorira* CGorira::Create(D3DXVECTOR3 pos,D3DXVECTOR3 size)
{
	CGorira *pGorira;
	pGorira = new CGorira;
	pGorira->Init(pos,size);
	
	pGorira->Load(GORIRA_TEX_NAME);
	return pGorira;
}
//=============================================================================
// ポリゴンの初期化処理
//=============================================================================
HRESULT CGorira :: Init(void)
{
	CScene2D::Init();


	return S_OK;
}
//=============================================================================
// ポリゴンの初期化処理(オーバーロード)
//=============================================================================
HRESULT CGorira :: Init(D3DXVECTOR3 pos,D3DXVECTOR3 size)
{
	CScene2D::Init(pos,size);

	CScene2D::SetTexture(ANIM_PAT_X,ANIM_PAT_Y,m_animPat);

	return S_OK;
}
//=============================================================================
// ポリゴンの終了処理
//=============================================================================
void CGorira :: Uninit(void)
{
	CScene2D::Uninit();
}
//=============================================================================
// ポリゴンの更新処理
//=============================================================================
void CGorira :: Update(void)
{
	D3DXVECTOR3 posGorira = GetPosition();
	D3DXVECTOR3 sizeGorira = GetSize();
	m_time++;
	if(m_time%ANIM_CANGE_FRAME == 0)
	{
		m_animPat++;
	}
	if(posGorira.x > 340)
	{
		posGorira.x-=2;
	}
	//if(posGorira.x < -sizeGorira.x)
	//{
	//	posGorira.x = SCREEN_WIDTH + 300/2;
	//}
	CScene2D::SetTexture(ANIM_PAT_X,ANIM_PAT_Y,m_animPat);

	this->CGorira::SetPosition(posGorira);
}
//=============================================================================
// ポリゴンの描画処理
//=============================================================================
void CGorira :: Draw(void)
{
	CScene2D::Draw();
}