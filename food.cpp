//=============================================================================
//
//=============================================================================
CFood *CFood::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	CFood *pFood;
	pFood = new CFood;

	pFood->Init(pos, size);
	
	//テクスチャの割り当て
	pFood->BindTexture( m_pTexture);

	return pFood;
}
//=============================================================================
//テクスチャのロード
//=============================================================================
HRESULT CFood::Load(void)
{
	if( m_pTexture == NULL)
	{
		LPDIRECT3DDEVICE9 pDevice;
		pDevice = CManager::GetRenderer()->GetDevice();

		// テクスチャの読み込み
		D3DXCreateTextureFromFile( pDevice, TEXTURENAME, &m_pTexture);
	}

	return S_OK;
}

//=============================================================================
//テクスチャのアンロード
//=============================================================================
void CFood::Unload(void)
{
	//テクスチャの破棄
	if( m_pTexture != NULL)
	{
		m_pTexture->Release();
		m_pTexture = NULL;
	}
}
