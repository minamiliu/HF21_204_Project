#define TEXTURENAME "data/TEXTURE/tomato.png"

//============================================
// 静的メンバー変数の初期化
//============================================
LPDIRECT3DTEXTURE9 CFood::m_pTexture = NULL;

//=============================================================================
//コンストラクタ
//=============================================================================
CFood::CFood()
{

}

//=============================================================================
//デストラクタ
//=============================================================================
CFood::~CFood()
{
	
}


//=============================================================================
// ポリゴンの初期化処理
//=============================================================================

