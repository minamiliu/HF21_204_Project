//=============================================================================
CBg::CBg()
{

}

//=============================================================================
//デストラクタ
//=============================================================================
CBg::~CBg()
{
	
}


//=============================================================================
// 背景の初期化処理
//=============================================================================
HRESULT CBg::Init(void)
{
	return S_OK;
}

HRESULT CBg::Init(D3DXVECTOR3 pos, D3DXVECTOR3 size, float speed)
{
	CScene2D::Init( pos, size);
	
	m_fSpeed = speed;

	return S_OK;
}

//=============================================================================
// 背景の終了処理
//=============================================================================
void CBg::Uninit(void)
{
	CScene2D::Uninit();
}


//=============================================================================
// 背景の更新処理
//=============================================================================
void CBg::Update(void)
{
	this->Scl( m_fSpeed, SCL_DOWN);
}

//=============================================================================
// 背景の描画処理
//=============================================================================
void CBg::Draw(void)
{
	CScene2D::Draw();
}

//=============================================================================
// 背景の生成処理
//=============================================================================
CBg *CBg::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, LPCSTR strFileName, float speed)
{
	CBg *pBg;
	pBg = new CBg;
	pBg->Init(pos, size, speed);

	//テクスチャの読み込み
	pBg->Load( strFileName);

	return pBg;
}
